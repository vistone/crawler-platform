// decryptXOR 使用指定的密钥对数据进行 XOR 异或解密（核心解密算法）
// 这是经过验证的实现，能正确处理Google Earth的数据
// keys: 解密密钥
// data: 要解密的数据（将被原地修改）
// offset: 数据起始偏移
// length: 要解密的长度
func decryptXOR(keys []byte, data []byte, offset, length int) {
	keyLen := 1016
	j := 16

	for i := 0; i < length; i++ {
		// XOR 解密：data[i] ^ keys[j+8]
		data[offset+i] ^= keys[j+8]
		j++

		// 密钥索引跳转逻辑（这是核心算法，不能改变）：
		// 1. 当索引为8的倍数时，跳过16个字节的密钥区域
		// 2. 当索引达到 keyLen 时，执行特殊回绕处理
		if j%8 == 0 {
			j += 16
		}
		if j >= keyLen {
			j = (j + 8) % 24
		}
	}
}
