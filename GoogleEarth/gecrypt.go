// GeDecrypt 解密函数（迁移自 libge）
// data: 要解密的数据（会被原地修改）
// key: 解密密钥（预期长度为 1024）
func GeDecrypt(data []byte, key []byte) {
	if len(data) == 0 || len(key) == 0 {
		return
	}
	// key 预期长度为 1024，算法与 C++ 版本一致
	i := 0
	j := 16
	for i < len(data) {
		kj := j + 8
		if kj >= len(key) {
			kj = kj % len(key)
		}
		data[i] ^= key[kj]
		i++
		j++
		if j%8 == 0 {
			j += 16
		}
		if j >= 1016 {
			j += 8
			j %= 24
		}
	}
}

// geDecrypt 私有包装函数，保持向后兼容
func geDecrypt(data []byte, key []byte) {
	GeDecrypt(data, key)
}

// UnpackGEZlib 解包（含可选解密）GE Zlib 数据：
// - 若前4字节为加密的ZLIB魔法数，则先解密再判断
// - 若为解密后的ZLIB魔法数，则按zlib流解压 payload
// - 其他情况直接返回原数据副本
func UnpackGEZlib(src []byte) ([]byte, error) {
	if len(src) < 4 {
		return nil, fmt.Errorf("input too short")
	}
	magic := binary.LittleEndian.Uint32(src[:4])
	// 若为加密的 ZLIB，则先解密
	if magic == CRYPTED_ZLIB_MAGIC {
		buf := make([]byte, len(src))
		copy(buf, src)
		geDecrypt(buf, CryptKey)
		src = buf
		magic = binary.LittleEndian.Uint32(src[:4])
	}
	// 解密后为 ZLIB 则解压
	if magic == DECRYPTED_ZLIB_MAGIC {
		if len(src) < 8 {
			return nil, fmt.Errorf("zlib payload too short")
		}
		uncompressSize := binary.LittleEndian.Uint32(src[4:8])
		zr, err := zlib.NewReader(bytes.NewReader(src[8:]))
		if err != nil {
			return nil, err
		}
		defer zr.Close()
		var out bytes.Buffer
		if _, err := io.Copy(&out, zr); err != nil {
			return nil, err
		}
		// 非强校验，仅记录期望长度
		_ = uncompressSize
		return out.Bytes(), nil
	}
	// 非压缩或非目标类型，直接返回拷贝
	out := make([]byte, len(src))
	copy(out, src)
	return out, nil
}

